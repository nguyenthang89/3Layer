        jDtNgaySinh.setDate((Date) jTableDocGia.getValueAt(jTableDocGia.getSelectedRow(), 5));
        jDtNgayLapThe.setDate((Date) jTableDocGia.getValueAt(jTableDocGia.getSelectedRow(), 6));
                
        
