    public int timKiemDocGia(String ten, String cccd, String email, Date ngaySinh, String diachi){

        String[] header = {"CCCD", "Tên độc giả", "Địa chỉ", "Loại độc giả", "Email", "Ngày sinh", "Ngày lập thẻ",  "Ngày hết hạn"};
        DefaultTableModel defaultTableModel = new DefaultTableModel(header,0);
        ArrayList<DocGiaDTO> arr = DocGiaBUS.timDocGia(ten, cccd, email, ngaySinh, diachi);
        DocGiaDTO docgia;
        for(int i = 0; i < arr.size(); i++){
            docgia = arr.get(i);            
            String cccdRow =  docgia.getcCCD();
            String hotenRow = docgia.getTenDocGia();
            String diachiRow = docgia.getDiaChi();
            String loaiRow = docgia.getLoaiDocGia();
            String emailRow = docgia.getEmail();
            Date ngaySinhRow = docgia.getNgaySinh();
            Date ngayLapTheRow = docgia.getNgayLapThe();
            Date ngayHetHanRow = docgia.getNgayHetHan();
            Object[] row = { cccdRow, hotenRow, diachiRow, loaiRow, emailRow, ngaySinhRow, ngayLapTheRow, ngayHetHanRow};
            defaultTableModel.addRow(row);
        }         

        jTableDocGia.setModel(defaultTableModel);        
        return arr.size();
    }
