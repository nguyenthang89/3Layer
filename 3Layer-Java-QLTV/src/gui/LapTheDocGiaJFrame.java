    private void jbtnXacNhanLapTheDocGiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnXacNhanLapTheDocGiaActionPerformed
       
        EmailValidate emailValidate = new EmailValidate();        
        if(jtxtCCCD.getText().isEmpty() || jtxtCCCD.getText().length() > 12 || jtxtCCCD.getText().length() < 9){
            JOptionPane.showMessageDialog(null, "Căn cước công dân không hợp lệ!", "THÔNG BÁO",
                                JOptionPane.WARNING_MESSAGE);
            jtxtCCCD.requestFocus();                
        }else if(jtxtHoVaTenDocGia.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập tên độc giả", "THÔNG BÁO",
                                JOptionPane.WARNING_MESSAGE);
            jtxtHoVaTenDocGia.requestFocus();
        }else if(jDtNgaySinh.getDate() == null){
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập ngày sinh độc giả", "THÔNG BÁO",
                                JOptionPane.WARNING_MESSAGE);
            jDtNgayLapThe.requestFocus();
        }else if(!emailValidate.validate(jtxtEmail.getText().trim())) {
            JOptionPane.showMessageDialog(null, "Email không hợp lệ!", "THÔNG BÁO",
                             JOptionPane.WARNING_MESSAGE);
            jtxtEmail.requestFocus();                
        } 
        else if(!jRadX.isSelected() && !jRadY.isSelected()){
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn loại độc giả", "THÔNG BÁO",
                                JOptionPane.WARNING_MESSAGE);
            jRadX.setBackground(Color.red);
            jRadY.setBackground(Color.red);
        }else{
            try {
                String rad ="" ;
                if(jRadX.isSelected()){
                    rad += jRadX.getText();
                }
                if(jRadY.isSelected()){
                    rad += jRadY.getText();
                }
                
                java.sql.Date ngaySinh = new java.sql.Date(jDtNgaySinh.getDate().getTime());
                java.sql.Date ngayLapThe = new java.sql.Date(jDtNgayLapThe.getDate().getTime());
                
                DocGiaDTO docgia = new DocGiaDTO();
                docgia.setcCCD(jtxtCCCD.getText());
                docgia.setTenDocGia(jtxtHoVaTenDocGia.getText());
                docgia.setDiaChi(jtxtDiaChi.getText());
                docgia.setEmail(jtxtEmail.getText());
                docgia.setLoaiDocGia(rad);
                docgia.setNgayLapThe(ngayLapThe);
                docgia.setNgaySinh(ngaySinh);
                
                int  i = DocGiaBUS.themDocGia(docgia);               
                
                switch (i) {
                    case 0:
                        JOptionPane.showMessageDialog(null, "Đã tồn tại mã độc giả ", "THẤT BẠI",
                                JOptionPane.ERROR_MESSAGE);
                        break; 
                    case -1:
                        JOptionPane.showMessageDialog(null, "Tuổi không hợp lệ! Kiểm tra lại ngày sinh!!!", "THẤT BẠI",
                                JOptionPane.ERROR_MESSAGE);
                        break;
                    case 1:
                        JOptionPane.showMessageDialog(null, "Tạo thẻ độc giả thành công", "THÀNH CÔNG", JOptionPane.INFORMATION_MESSAGE);
                        break;
                }
            } catch (Exception ex) {
                Logger.getLogger(LapTheDocGiaJFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        timKiemDocGia("", "", "", null, "");
        
    }//GEN-LAST:event_jbtnXacNhanLapTheDocGiaActionPerformed

