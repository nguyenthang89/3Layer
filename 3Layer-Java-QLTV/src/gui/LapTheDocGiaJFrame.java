    private void jbtnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnXoaActionPerformed
        // TODO add your handling code here:
        ArrayList<String> arr = new ArrayList<String>();
        
        if(jTableDocGia.getSelectedRow()> -1){
            int[] selectedrows  = jTableDocGia.getSelectedRows();
            for (int i = 0; i < selectedrows.length; i++){
                arr.add(jTableDocGia.getValueAt(selectedrows[i], 0).toString());
            }
        }
        
        try {
            int i = DocGiaBUS.Xoa(arr);
            if(i>0){
                JOptionPane.showMessageDialog(null, "Xóa thành công " +i + " độc giả" , "THÀNH CÔNG", JOptionPane.INFORMATION_MESSAGE);                        
            }
        } catch (Exception ex) {
            Logger.getLogger(LapTheDocGiaJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }        
        timKiemDocGia("", "", "", null, "");  
    }//GEN-LAST:event_jbtnXoaActionPerformed

